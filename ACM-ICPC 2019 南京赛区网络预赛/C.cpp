const LL mod = 998244353;

const int MAXN = 1 << 20;
int prime[MAXN];
LL phi[MAXN], powphi[MAXN];
bool is_prime[MAXN];

LL pow21[100005], pow21w[100005];

void prep() {
    pow21[0] = pow21w[0] = 1;
    for (int i = 1; i <= 100000; i++) pow21[i] = pow21[i - 1] * 2 % mod;
    for (int i = 1; i <= 100000; i++) pow21w[i] = pow21w[i - 1] * pow21[100000] % mod;
}

inline LL pow2(LL x) {
    return pow21[x % 100000] * pow21w[x / 100000] % mod;
}

LL powmod(LL b, LL e) {
    LL r = 1;
    while (e) {
        if (e & 1) r = r * b % mod;
        b = b * b % mod;
        e >>= 1;
    }
    return r;
}

int sieve(int n){
    int p=0;
    for(int i=0;i<=n;i++) is_prime[i]=true;
    is_prime[0]=is_prime[1]=false;
    memset(phi,0,sizeof(phi));
    phi[1]=1;
    for(int i=2;i<=n;i++)
    {
        if(is_prime[i]) {prime[p++]=i; phi[i]=i-1;}
        for(int j=0;j<p;j++)
        {
            if(prime[j]*i>n) break;
            is_prime[prime[j]*i]=false;
            phi[i*prime[j]]=phi[i]*(i%prime[j]?prime[j]-1:prime[j]);
            if(i%prime[j]==0) break;
        }
    }
    for (int i = 1; i <= n; i++) powphi[i] = powmod(2, phi[i]);
    return p;
}

LL calc(int n) {
    LL tot = 0;
    for (int i = 1; i <= n; i++) {
        LL cur = phi[i] * phi[n] % mod * pow2(phi[i] * phi[n]) % mod;
        tot += cur; if (i < n) tot += cur;
    }
    return tot % mod;
}

